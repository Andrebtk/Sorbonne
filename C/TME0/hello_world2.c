void affiche(const char message[]) {
  if (message[0] == '\0') return;
  for (int i =0; i < N_LIGNES; i++) {
    for (int indice = 0; message[indice] != '\0'; indice++) {
      char caractere = message[indice];
      int indiceCaractere = caractere - 'A';
      for (int j = 0; j < N_COLONNES; j++) {
        if (caractere == ' ') printf ("%c",  espace[i][j]);
        else                  printf ("%c",  lettre[indiceCaractere][i][j]);
      } 
    }
    printf ("\n");
  }
}

